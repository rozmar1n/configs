require "nvchad.options"

local opt = vim.opt

opt.tabstop = 4       -- Количество пробелов, которое представляет символ табуляции
opt.softtabstop = 4    -- Количество пробелов, которое используется при нажатии Tab в режиме вставки
opt.shiftwidth = 4     -- Количество пробелов для каждого уровня отступа
opt.expandtab = true   -- Использовать пробелы вместо символов табуляции


